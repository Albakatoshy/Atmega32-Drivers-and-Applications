#include <stdio.h>

int main(){
    int num1, num2, num3;
    printf("Enter the first number: ");
    scanf("%d", &num1);
    printf("Enter the second number: ");
    scanf("%d", &num2);
    printf("Enter the third number: ");
    scanf("%d", &num3);

    printf("num3->%d\n", num3);
    printf("num2->%d\n", num2);
    printf("num1->%d\n", num1);
    

    return 0;
}